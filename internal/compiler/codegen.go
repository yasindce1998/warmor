func writeRuleCheck(b *strings.Builder, rule Rule, eventType string) {
	action := actionConstant(rule.Action)

	condExpr := generateConditionExpr(rule.Conditions, eventType)
	if condExpr == "" {
		condExpr = "true"
	}

	b.WriteString(fmt.Sprintf("    // Rule: %s\n", rule.Name))
	b.WriteString(fmt.Sprintf("    if %s {\n", condExpr))
	b.WriteString(fmt.Sprintf("        return %s;\n", action))
	b.WriteString("    }\n\n")
}

func generateConditionExpr(conds Conditions, eventType string) string {
	var parts []string

	if len(conds.All) > 0 {
		allParts := make([]string, 0, len(conds.All))
		for _, c := range conds.All {
			expr := conditionToRust(c, eventType)
			if expr != "" {
				allParts = append(allParts, expr)
			}
		}
		if len(allParts) > 0 {
			parts = append(parts, strings.Join(allParts, " && "))
		}
	}

	if len(conds.Any) > 0 {
		anyParts := make([]string, 0, len(conds.Any))
		for _, c := range conds.Any {
			expr := conditionToRust(c, eventType)
			if expr != "" {
				anyParts = append(anyParts, expr)
			}
		}
		if len(anyParts) > 0 {
			parts = append(parts, fmt.Sprintf("(%s)", strings.Join(anyParts, " || ")))
		}
	}

	if len(conds.Not) > 0 {
		for _, c := range conds.Not {
			expr := conditionToRust(c, eventType)
			if expr != "" {
				parts = append(parts, fmt.Sprintf("!(%s)", expr))
			}
		}
	}

	return strings.Join(parts, " && ")
}

func conditionToRust(c Condition, eventType string) string {
	fieldExpr := fieldToRust(c.Field, eventType)

	switch c.Operator {
	case "eq":
		return formatEq(fieldExpr, c.Field, c.Value)
	case "not":
		return formatNot(fieldExpr, c.Field, c.Value)
	case "glob":
		pattern, _ := c.Value.(string)
		return fmt.Sprintf("matches_glob(&%s, %q)", fieldExpr, pattern)
	case "any_of":
		return formatAnyOf(fieldExpr, c.Field, c.Value)
	case "none_of":
		return formatNoneOf(fieldExpr, c.Field, c.Value)
	case "gt":
		return fmt.Sprintf("%s > %s", fieldExpr, formatNumeric(c.Value))
	case "lt":
		return fmt.Sprintf("%s < %s", fieldExpr, formatNumeric(c.Value))
	case "gte":
		return fmt.Sprintf("%s >= %s", fieldExpr, formatNumeric(c.Value))
	case "lte":
		return fmt.Sprintf("%s <= %s", fieldExpr, formatNumeric(c.Value))
	case "starts_with":
		s, _ := c.Value.(string)
		return fmt.Sprintf("%s.starts_with(%q)", fieldExpr, s)
	case "contains":
		s, _ := c.Value.(string)
		return fmt.Sprintf("%s.contains(%q)", fieldExpr, s)
	default:
		return "true"
	}
}

func fieldToRust(field string, eventType string) string {
	switch field {
	case "path":
		if eventType == "process" {
			return "event.filename"
		}
		return "event.path"
	case "args":
		return "event.filename"
	default:
		return "event." + field
	}
}

func isNumericField(field string) bool {
	switch field {
	case "pid", "uid", "gid", "flags", "remote_port", "local_port":
		return true
	default:
		return false
	}
}

func fieldRustType(field string) string {
	switch field {
	case "pid", "uid", "gid", "flags":
		return "u32"
	case "remote_port", "local_port":
		return "u16"
	default:
		return "str"
	}
}

func formatEq(fieldExpr, field string, value interface{}) string {
	if isNumericField(field) {
		return fmt.Sprintf("%s == %s", fieldExpr, formatNumeric(value))
	}
	s := fmt.Sprintf("%v", value)
	return fmt.Sprintf("%s == %q", fieldExpr, s)
}

func formatNot(fieldExpr, field string, value interface{}) string {
	if isNumericField(field) {
		return fmt.Sprintf("%s != %s", fieldExpr, formatNumeric(value))
	}
	s := fmt.Sprintf("%v", value)
	return fmt.Sprintf("%s != %q", fieldExpr, s)
}

func formatAnyOf(fieldExpr, field string, value interface{}) string {
	items := toSlice(value)
	if isNumericField(field) {
		rustType := fieldRustType(field)
		vals := make([]string, 0, len(items))
		for _, item := range items {
			vals = append(vals, formatNumeric(item))
		}
		return fmt.Sprintf("any_of_%s(%s, &[%s])", rustType, fieldExpr, strings.Join(vals, ", "))
	}
	vals := make([]string, 0, len(items))
	for _, item := range items {
		vals = append(vals, fmt.Sprintf("%q", fmt.Sprintf("%v", item)))
	}
	return fmt.Sprintf("any_of_str(&%s, &[%s])", fieldExpr, strings.Join(vals, ", "))
}

func formatNoneOf(fieldExpr, field string, value interface{}) string {
	items := toSlice(value)
	if isNumericField(field) {
		rustType := fieldRustType(field)
		vals := make([]string, 0, len(items))
		for _, item := range items {
			vals = append(vals, formatNumeric(item))
		}
		return fmt.Sprintf("none_of_%s(%s, &[%s])", rustType, fieldExpr, strings.Join(vals, ", "))
	}
	vals := make([]string, 0, len(items))
	for _, item := range items {
		vals = append(vals, fmt.Sprintf("%q", fmt.Sprintf("%v", item)))
	}
	return fmt.Sprintf("none_of_str(&%s, &[%s])", fieldExpr, strings.Join(vals, ", "))
}

func formatNumeric(value interface{}) string {
	switch v := value.(type) {
	case int:
		return fmt.Sprintf("%d", v)
	case int64:
		return fmt.Sprintf("%d", v)
	case float64:
		if v == float64(int64(v)) {
			return fmt.Sprintf("%d", int64(v))
		}
		return fmt.Sprintf("%v", v)
	default:
		return fmt.Sprintf("%v", v)
	}
}

func toSlice(value interface{}) []interface{} {
	switch v := value.(type) {
	case []interface{}:
		return v
	case []string:
		result := make([]interface{}, len(v))
		for i, s := range v {
			result[i] = s
		}
		return result
	default:
		return []interface{}{value}
	}
}

func rulesForEvent(policy *Policy, eventType string) []Rule {
	var rules []Rule
	for _, r := range policy.Rules {
		if r.Event == eventType {
			rules = append(rules, r)
		}
	}
	return rules
}

func actionConstant(action string) string {
	switch action {
	case "allow":
		return "ACTION_ALLOW"
	case "deny":
		return "ACTION_DENY"
	case "log":
		return "ACTION_LOG"
	default:
		return "ACTION_ALLOW"
	}
}

