func GenerateRust(policy *Policy) (string, error) {
	var b strings.Builder

	b.WriteString(fmt.Sprintf("// Auto-generated by warmor-compile from: %s\n", policy.Name))
	b.WriteString("// DO NOT EDIT — regenerate from the source YAML policy.\n\n")
	b.WriteString("#![no_std]\n\n")
	b.WriteString("const ACTION_ALLOW: i32 = 0;\nconst ACTION_DENY: i32 = 1;\nconst ACTION_LOG: i32 = 2;\nconst ACTION_AUDIT_DENY: i32 = 3;\n\n")

	// Event type constants matching Go's binary layout
	b.WriteString("const EVENT_PROCESS: u8 = 0;\nconst EVENT_FILE: u8 = 1;\nconst EVENT_NETWORK: u8 = 2;\n\n")

	// Binary struct field offsets
	writeBinaryOffsets(&b)
	writeHelperFunctions(&b)
	writeMallocFree(&b)
	writeReasonTable(&b, policy)
	writeEvaluateEventFunction(&b, policy)
	writeGetLastMatchedRule(&b)

	// Host callback extern declarations
	writeHostExterns(&b)

	return b.String(), nil
}

