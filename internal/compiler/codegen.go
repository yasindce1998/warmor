func writeRuleCheckBinary(b *strings.Builder, rule Rule, eventType string, ruleIndex int) {
	action := actionConstant(rule.Action)
	if rule.Mode == "audit" && rule.Action == "deny" {
		action = "ACTION_AUDIT_DENY"
	}

	condExpr := generateConditionExprBinary(rule.Conditions, eventType)
	if condExpr == "" {
		condExpr = "true"
	}

	b.WriteString(fmt.Sprintf("    // Rule: %s\n", rule.Name))
	b.WriteString(fmt.Sprintf("    if %s {\n", condExpr))
	b.WriteString(fmt.Sprintf("        LAST_MATCHED = %d;\n", ruleIndex))
	b.WriteString(fmt.Sprintf("        return %s;\n", action))
	b.WriteString("    }\n\n")
}

func writeGetLastMatchedRule(b *strings.Builder) {
	b.WriteString(`#[no_mangle]
pub extern "C" fn get_last_matched_rule(buf: *mut u8, len: u32) -> u32 {
    unsafe {
        if LAST_MATCHED == 0xFFFFFFFF {
            return 0;
        }
        let idx = LAST_MATCHED as usize;
        if idx >= RULE_REASONS.len() {
            return 0;
        }
        let reason = RULE_REASONS[idx];
        let bytes = reason.as_bytes();
        let copy_len = if bytes.len() < len as usize { bytes.len() } else { len as usize };
        core::ptr::copy_nonoverlapping(bytes.as_ptr(), buf, copy_len);
        copy_len as u32
    }
}

`)
}

func writeHostExterns(b *strings.Builder) {
	b.WriteString(`// Host callback extern declarations (available for hand-written WASM extensions)
extern "C" {
    #[allow(dead_code)]
    fn get_field_u32(field_id: u32) -> u32;
    #[allow(dead_code)]
    fn get_field_str(field_id: u32, buf_ptr: *mut u8, buf_len: u32) -> u32;
}
`)
}

func generateConditionExprBinary(conds Conditions, eventType string) string {
	var parts []string

	if len(conds.All) > 0 {
		allParts := make([]string, 0, len(conds.All))
		for _, c := range conds.All {
			expr := conditionToRustBinary(c, eventType)
			if expr != "" {
				allParts = append(allParts, expr)
			}
		}
		if len(allParts) > 0 {
			parts = append(parts, strings.Join(allParts, " && "))
		}
	}

	if len(conds.Any) > 0 {
		anyParts := make([]string, 0, len(conds.Any))
		for _, c := range conds.Any {
			expr := conditionToRustBinary(c, eventType)
			if expr != "" {
				anyParts = append(anyParts, expr)
			}
		}
		if len(anyParts) > 0 {
			parts = append(parts, fmt.Sprintf("(%s)", strings.Join(anyParts, " || ")))
		}
	}

	if len(conds.Not) > 0 {
		for _, c := range conds.Not {
			expr := conditionToRustBinary(c, eventType)
			if expr != "" {
				parts = append(parts, fmt.Sprintf("!(%s)", expr))
			}
		}
	}

	return strings.Join(parts, " && ")
}

func conditionToRustBinary(c Condition, eventType string) string {
	fieldExpr := fieldToRustBinary(c.Field, eventType)

	switch c.Operator {
	case "eq":
		return formatEqBinary(fieldExpr, c.Field, c.Value)
	case "not":
		return formatNotBinary(fieldExpr, c.Field, c.Value)
	case "glob":
		pattern, _ := c.Value.(string)
		return fmt.Sprintf("matches_glob(%s, %q)", fieldExpr, pattern)
	case "any_of":
		return formatAnyOfBinary(fieldExpr, c.Field, c.Value)
	case "none_of":
		return formatNoneOfBinary(fieldExpr, c.Field, c.Value)
	case "gt":
		return fmt.Sprintf("%s > %s", fieldExpr, formatNumeric(c.Value))
	case "lt":
		return fmt.Sprintf("%s < %s", fieldExpr, formatNumeric(c.Value))
	case "gte":
		return fmt.Sprintf("%s >= %s", fieldExpr, formatNumeric(c.Value))
	case "lte":
		return fmt.Sprintf("%s <= %s", fieldExpr, formatNumeric(c.Value))
	case "starts_with":
		s, _ := c.Value.(string)
		return fmt.Sprintf("str_starts_with(%s, %q)", fieldExpr, s)
	case "contains":
		s, _ := c.Value.(string)
		return fmt.Sprintf("str_contains(%s, %q)", fieldExpr, s)
	default:
		return "true"
	}
}

func fieldToRustBinary(field string, eventType string) string {
	switch field {
	case "pid":
		return "pid"
	case "uid":
		return "uid"
	case "gid":
		return "gid"
	case "comm":
		return "comm"
	case "path":
		if eventType == "process" {
			return "path"
		}
		return "path"
	case "args":
		return "path"
	case "operation":
		return "operation"
	case "protocol":
		return "protocol"
	case "remote_addr":
		return "remote_addr"
	case "flags":
		return "flags"
	case "remote_port":
		return "remote_port"
	case "local_port":
		return "local_port"
	default:
		return field
	}
}

func formatEqBinary(fieldExpr, field string, value interface{}) string {
	if isNumericField(field) {
		return fmt.Sprintf("%s == %s", fieldExpr, formatNumeric(value))
	}
	s := fmt.Sprintf("%v", value)
	return fmt.Sprintf("%s == %q", fieldExpr, s)
}

func formatNotBinary(fieldExpr, field string, value interface{}) string {
	if isNumericField(field) {
		return fmt.Sprintf("%s != %s", fieldExpr, formatNumeric(value))
	}
	s := fmt.Sprintf("%v", value)
	return fmt.Sprintf("%s != %q", fieldExpr, s)
}

func formatAnyOfBinary(fieldExpr, field string, value interface{}) string {
	items := toSlice(value)
	if isNumericField(field) {
		rustType := fieldRustType(field)
		vals := make([]string, 0, len(items))
		for _, item := range items {
			vals = append(vals, formatNumeric(item))
		}
		return fmt.Sprintf("any_of_%s(%s, &[%s])", rustType, fieldExpr, strings.Join(vals, ", "))
	}
	vals := make([]string, 0, len(items))
	for _, item := range items {
		vals = append(vals, fmt.Sprintf("%q", fmt.Sprintf("%v", item)))
	}
	return fmt.Sprintf("any_of_str(%s, &[%s])", fieldExpr, strings.Join(vals, ", "))
}

func formatNoneOfBinary(fieldExpr, field string, value interface{}) string {
	items := toSlice(value)
	if isNumericField(field) {
		rustType := fieldRustType(field)
		vals := make([]string, 0, len(items))
		for _, item := range items {
			vals = append(vals, formatNumeric(item))
		}
		return fmt.Sprintf("none_of_%s(%s, &[%s])", rustType, fieldExpr, strings.Join(vals, ", "))
	}
	vals := make([]string, 0, len(items))
	for _, item := range items {
		vals = append(vals, fmt.Sprintf("%q", fmt.Sprintf("%v", item)))
	}
	return fmt.Sprintf("none_of_str(%s, &[%s])", fieldExpr, strings.Join(vals, ", "))
}

func globalRuleIndex(policy *Policy, rule Rule) int {
	for i, r := range policy.Rules {
		if r.Name == rule.Name {
			return i
		}
	}
	return 0
}

func isNumericField(field string) bool {
	switch field {
	case "pid", "uid", "gid", "flags", "remote_port", "local_port":
		return true
	default:
		return false
	}
}

func fieldRustType(field string) string {
	switch field {
	case "pid", "uid", "gid", "flags":
		return "u32"
	case "remote_port", "local_port":
		return "u16"
	default:
		return "str"
	}
}

func formatNumeric(value interface{}) string {
	switch v := value.(type) {
	case int:
		return fmt.Sprintf("%d", v)
	case int64:
		return fmt.Sprintf("%d", v)
	case float64:
		if v == float64(int64(v)) {
			return fmt.Sprintf("%d", int64(v))
		}
		return fmt.Sprintf("%v", v)
	default:
		return fmt.Sprintf("%v", v)
	}
}

func toSlice(value interface{}) []interface{} {
	switch v := value.(type) {
	case []interface{}:
		return v
	case []string:
		result := make([]interface{}, len(v))
		for i, s := range v {
			result[i] = s
		}
		return result
	default:
		return []interface{}{value}
	}
}

func rulesForEvent(policy *Policy, eventType string) []Rule {
	var rules []Rule
	for _, r := range policy.Rules {
		if r.Event == eventType {
			rules = append(rules, r)
		}
	}
	return rules
}

func actionConstant(action string) string {
	switch action {
	case "allow":
		return "ACTION_ALLOW"
	case "deny":
		return "ACTION_DENY"
	case "log":
		return "ACTION_LOG"
	default:
		return "ACTION_ALLOW"
	}
}

