func GenerateRust(policy *Policy) (string, error) {
	var b strings.Builder

	b.WriteString(fmt.Sprintf("// Auto-generated by warmor-compile from: %s\n", policy.Name))
	b.WriteString("// DO NOT EDIT — regenerate from the source YAML policy.\n\n")
	b.WriteString("use serde::Deserialize;\nuse std::slice;\n\n")
	b.WriteString("const ACTION_ALLOW: i32 = 0;\nconst ACTION_DENY: i32 = 1;\nconst ACTION_LOG: i32 = 2;\n\n")

	writeEventStructs(&b)
	writeHelperFunctions(&b)
	writeMallocFree(&b)
	writeEvaluateFunction(&b, policy)

	return b.String(), nil
}

